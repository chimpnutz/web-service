package com.elp.model;

public class NetworkManagementModel {
	
	int companyid;
	String companyname;
	String companyaddress;
	String retailersim;
	String mobile;
	String opensim;
	String contactperson;
	String level;
	String parentcompany;
	String status;
	String upload;
	
	
	
	public int getCompanyid() {
		return companyid;
	}
	public void setCompanyid(int companyid) {
		this.companyid = companyid;
	}
	public String getCompanyname() {
		return companyname;
	}
	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}
	public String getCompanyaddress() {
		return companyaddress;
	}
	public void setCompanyaddress(String companyaddress) {
		this.companyaddress = companyaddress;
	}
	public String getRetailersim() {
		return retailersim;
	}
	public void setRetailersim(String retailersim) {
		this.retailersim = retailersim;
	}
	public String getContactperson() {
		return contactperson;
	}
	public void setContactperson(String contactperson) {
		this.contactperson = contactperson;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getParentcompany() {
		return parentcompany;
	}
	public void setParentcompany(String parentcompany) {
		this.parentcompany = parentcompany;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getUpload() {
		return upload;
	}
	public void setUpload(String upload) {
		this.upload = upload;
	}
	public String getOpensim() {
		return opensim;
	}
	public void setOpensim(String opensim) {
		this.opensim = opensim;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	

}
