package com.circles.model;

public class Image {

	private String id;
	private String application_id;
	private String filename;
	private String ispushed;
	private String isbeingpushed;
	private String type;
	private String number;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getApplication_id() {
		return application_id;
	}
	public void setApplication_id(String application_id) {
		this.application_id = application_id;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getIspushed() {
		return ispushed;
	}
	public void setIspushed(String ispushed) {
		this.ispushed = ispushed;
	}
	public String getIsbeingpushed() {
		return isbeingpushed;
	}
	public void setIsbeingpushed(String isbeingpushed) {
		this.isbeingpushed = isbeingpushed;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	
	 
	
}
