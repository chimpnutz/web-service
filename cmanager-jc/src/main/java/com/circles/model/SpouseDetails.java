package com.circles.model;

public class SpouseDetails {
	
	String spousefullname;
	String spousecontact;
	long spousebirthday;
	
	public String getSpousefullname() {
		return spousefullname;
	}
	public void setSpousefullname(String spousefullname) {
		this.spousefullname = spousefullname;
	}
	public String getSpousecontact() {
		return spousecontact;
	}
	public void setSpousecontact(String spousecontact) {
		this.spousecontact = spousecontact;
	}
	public long getSpousebirthday() {
		return spousebirthday;
	}
	public void setSpousebirthday(long spousebirthday) {
		this.spousebirthday = spousebirthday;
	}

}
