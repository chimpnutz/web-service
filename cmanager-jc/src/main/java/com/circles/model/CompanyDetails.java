package com.circles.model;

public class CompanyDetails {
	
	String companyname;
	String companynature;
	String companyauth;
	String companypos;
	
	public String getCompanyname() {
		return companyname;
	}
	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}
	public String getCompanynature() {
		return companynature;
	}
	public void setCompanynature(String companynature) {
		this.companynature = companynature;
	}
	public String getCompanyauth() {
		return companyauth;
	}
	public void setCompanyauth(String companyauth) {
		this.companyauth = companyauth;
	}
	public String getCompanypos() {
		return companypos;
	}
	public void setCompanypos(String companypos) {
		this.companypos = companypos;
	}

}
