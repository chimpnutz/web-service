package com.elp.model;

public class EmergencyLoadManagementModel {
	
	String companyname;
	String amount;
	String validfr;
	String validto;
	String status;
	
	String paymentstatus;
	String deliverystatus;
	String availeddate;
	
	public String getCompanyname() {
		return companyname;
	}
	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getValidfr() {
		return validfr;
	}
	public void setValidfr(String validfr) {
		this.validfr = validfr;
	}
	public String getValidto() {
		return validto;
	}
	public void setValidto(String validto) {
		this.validto = validto;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPaymentstatus() {
		return paymentstatus;
	}
	public void setPaymentstatus(String paymentstatus) {
		this.paymentstatus = paymentstatus;
	}
	public String getDeliverystatus() {
		return deliverystatus;
	}
	public void setDeliverystatus(String deliverystatus) {
		this.deliverystatus = deliverystatus;
	}
	public String getAvaileddate() {
		return availeddate;
	}
	public void setAvaileddate(String availeddate) {
		this.availeddate = availeddate;
	}

}
