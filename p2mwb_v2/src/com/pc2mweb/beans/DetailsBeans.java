package com.pc2mweb.beans;

public class DetailsBeans {
	
	String amaxhost;
	String amaxuri;
	String ipaddress;
	String appname;
	String mobileno;
	String amount;
	
	public String getAmaxhost() {
		return amaxhost;
	}
	public void setAmaxhost(String amaxhost) {
		this.amaxhost = amaxhost;
	}
	public String getAmaxuri() {
		return amaxuri;
	}
	public void setAmaxuri(String amaxuri) {
		this.amaxuri = amaxuri;
	}
	public String getIpaddress() {
		return ipaddress;
	}
	public void setIpaddress(String ipaddress) {
		this.ipaddress = ipaddress;
	}
	public String getAppname() {
		return appname;
	}
	public void setAppname(String appname) {
		this.appname = appname;
	}
	public String getMobileno() {
		return mobileno;
	}
	public void setMobileno(String mobileno) {
		this.mobileno = mobileno;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
}
