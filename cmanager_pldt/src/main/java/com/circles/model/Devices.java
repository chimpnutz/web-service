package com.circles.model;

public class Devices {
	
	private String deviceid;
	private String device_name;
	private String description;
	private String device_image;
	private String created;
	private String updated;
	private String edited_by;
	private String version;
	private String ispushed;
	
	public String getDeviceid() {
		return deviceid;
	}
	public void setDeviceid(String deviceid) {
		this.deviceid = deviceid;
	}
	public String getDevice_name() {
		return device_name;
	}
	public void setDevice_name(String device_name) {
		this.device_name = device_name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDevice_image() {
		return device_image;
	}
	public void setDevice_image(String device_image) {
		this.device_image = device_image;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	public String getEdited_by() {
		return edited_by;
	}
	public void setEdited_by(String edited_by) {
		this.edited_by = edited_by;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getIspushed() {
		return ispushed;
	}
	public void setIspushed(String ispushed) {
		this.ispushed = ispushed;
	}

}
