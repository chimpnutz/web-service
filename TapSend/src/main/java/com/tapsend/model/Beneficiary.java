package com.tapsend.model;

public class Beneficiary {
	
	private String ben_parentid;
	private String firstName;
	private String middleName;
	private String lastName;
	private String suffix;
	private String birthday;
	private String gender;
	private String civil_status;
	private String maiden_name;
	private String disabled;
	private String ispushed;
	
	public String getBen_parentid() {
		return ben_parentid;
	}
	public void setBen_parentid(String ben_parentid) {
		this.ben_parentid = ben_parentid;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getSuffix() {
		return suffix;
	}
	public void setSuffix(String suffix) {
		this.suffix = suffix;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getCivil_status() {
		return civil_status;
	}
	public void setCivil_status(String civil_status) {
		this.civil_status = civil_status;
	}
	public String getMaiden_name() {
		return maiden_name;
	}
	public void setMaiden_name(String maiden_name) {
		this.maiden_name = maiden_name;
	}
	public String getDisabled() {
		return disabled;
	}
	public void setDisabled(String disabled) {
		this.disabled = disabled;
	}
	public String getIspushed() {
		return ispushed;
	}
	public void setIspushed(String ispushed) {
		this.ispushed = ispushed;
	}

}
